package modelo;

public class EfeitoCardsArmadilha {
	
	public void BloqueadorDeMagia() {
		// Quando um Card de Magia for ativado: descarte 1 card; negue a ativação e, se isso acontecer, destrua-o.
	}
	
	public void BuracoArmadilha() {
		// Quando seu oponente Invocar por Invocação-Normal ou Virar 1 monstro com 1000 ou mais de ATK: escolha o 
		// monstro; destrua o alvo.
	}
	
	public void CartolasMagicas() {
		// Durante a Fase de Batalha do seu oponente: selecione 2 Cards de Magia/Armadilha do seu Deck e 1 monstro 
		// que você controla. Invoque-os por Invocação-Especial como Monstros Normais (ATK 0/DEF 0) com a face 
		// para baixo em Posição de Defesa. Baixe o monstro selecionado se ele estava com a face para cima e 
		// embaralhe-os no campo. Os 2 cards selecionados do seu Deck são destruídos no final da Fase de Batalha e 
		// não podem permanecer no campo, exceto durante esta Fase de Batalha.
	}
	
	public void ChamadoDosAssombrados() {
		// Ative este card ao escolher 1 monstro no seu Cemitério; Invoque o alvo por Invocação-Especial com a 
		 //face para cima em Posição de Ataque. Quando este card deixar o campo, destrua o alvo. 
		// Quando o alvo for destruído, destrua este card.
	}
	
	public void CilindroMagico() {
		// Quando um monstro do oponente declarar um ataque: escolha o monstro atacante; negue o ataque e, 
		// se isso acontecer, cause dano ao seu oponente igual ao ATK do monstro.
	}
	
	public void CirculoDaProfecia() {
		// Ative esse card ao escolher 1 monstro que seu oponente controla; ele não pode atacar ou mudar sua 
		// posição de batalha. Quando esse monstro for destruído, destrua este card.
	}
	
	public void ForcaEspelho() {
		// Quando um monstro do oponente declarar um ataque: destrua todos os monstros em Posição de Ataque 
		// que seu oponente controla.
	}
	
	public void LadraoDeTumulos() {
		// Selecione 1 Spell Card no Cemitério do seu oponente. O efeito desta carta torna-se o mesmo efeito 
		// da carta selecionada, somente até o fim da End Phase. Se o fizer, você recebe 2000 de dano.
	}
	
	public void OferendaDerradeira() {
		// Durante sua Fase Principal ou Fase de Batalha do seu oponente: você pode pagar 500 Pontos de Vida; 
		// imediatamente após este efeito resolver, Invoque por Invocação-Normal/Baixar 1 monstro.
	}
	
	public void OlhoDaVerdade() {
		// Seu oponente deve manter sua mão revelada. Durante cada Fase de Apoio do seu oponente, se ele tiver 
		// um Card de Magia na mão dele, ele ganha 1000 PV.
	}
	

}
